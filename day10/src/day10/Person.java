package day10;

public class Person {
	
	
	private String person;
	private String vip;
	
	
	public Person(String person, String vip) {
		super();
		this.person = person;
		this.vip = vip;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getVip() {
		return vip;
	}
	public void setVip(String vip) {
		this.vip = vip;
	}
	

}
